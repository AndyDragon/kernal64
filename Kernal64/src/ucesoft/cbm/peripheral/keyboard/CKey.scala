	  def is128Key(k:Key): Boolean = (k.id & 0x100) > 0
		def isCBM2Key(k:Key): Boolean = (k.id & 0x200) > 0
	  
	  def getRowCol(k:Key): (Int,Int) = {
	    val id = k.id
	    val col = id & 0xf
	    val row = (id >> 4) & 0xf
	    (row,col)
	  }
